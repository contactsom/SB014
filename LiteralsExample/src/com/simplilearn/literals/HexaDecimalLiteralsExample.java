package com.simplilearn.literals;

public class HexaDecimalLiteralsExample {

	public static void main(String[] args) {
		
		int x = 0x10;
		System.out.println(x);
		
		int a = 0xFACE;
		System.out.println(a);
		
		/*
		 * A = 10
		 * B = 11
		 * C = 12
		 * D = 13 
		 * E = 14
		 * F = 15
		 * */
	}
}
