package com.simplilearn.literals;

public class CharacterLiteralsExample {

	public static void main(String[] args) {
		
		char a1 = 'a';
		System.out.println(a1);
		
		char a2 = 97; // Unicode Decimal Representation
		System.out.println(a2);
		
		char a3 = '\u0061'; // Unicode Code Representation
		System.out.println(a3);
		
		char a4 = 0141;
		System.out.println(a4); // Unicode Octal Representation
		
		char a5 = '\u0047';
		System.out.println(a5);
		
		//U+0047	G	71	0107
		
		
		System.out.println("*********************************************");
		System.out.print("Line-1: Welcomne to Simplilearn \n");
		System.out.print("Line-2:We are Learning Char Literals \n");
		
		
		System.out.println("*********************************************");
		System.out.print("Line-1: Welcomne to Simplilearn \t");
		System.out.print("Line-2:We are Learning Char Literals");
		
		
		System.out.println("-------------------------------------");
		System.out.print("Line-1: Welcomne to  \r Simplilearn ");
		
		
		
		
		
		
		
		
		
		
		
	}
}
