package com.simplilearn.datatype;

public class FloatExample {

	public static void main(String[] args) {
		
		float f1= 10.5F;
		System.out.println(f1);

		float f2= 90.5f;
		System.out.println(f2);
		
		float f3= 20f;
		System.out.println(f3);
		
		
		float f4 = 22/7;
		System.out.println(f4);
	}

}
