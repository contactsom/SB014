package com.simplilearn.switchcase;

public class Lab1 {

	public static void main(String[] args) {
	
		byte x = 101;
		
		switch(x) {
			case 101:
				System.out.println("I am case 101");
			case 11:
				System.out.println("I am case 11");
			case 12:
				System.out.println("I am case 12");
			case 13:
				System.out.println("I am case 13");
		}
		
		

	}

}
