package com.simplilearn.switchcase;

public class Lab3 {

	public static void main(String[] args) {
	
		char ch = 'A';
		
		switch(ch) {
			case 'B':
				System.out.println("I am case B");
			case 'C':
				System.out.println("I am case C");
			case 'D':
				System.out.println("I am case D");
			case 'A':
				System.out.println("I am case A");
		}
		
		

	}

}
