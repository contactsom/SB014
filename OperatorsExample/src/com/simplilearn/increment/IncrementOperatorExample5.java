package com.simplilearn.increment;

public class IncrementOperatorExample5 {

	public static void main(String[] args) {
	
		int x = 20;
		
		System.out.println("Before Decrement Value of X is : "+x); // 20
		
		int y = --x;
		System.out.println("After Decrement Value of X is  : "+x); //19
		
		System.out.println("What is the Value of Y  : "+y); // 19
		
		

	}

}
