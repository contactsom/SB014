package com.simplilearn.increment;

public class IncrementOperatorExample3 {

	public static void main(String[] args) {
	
		int x = 20;
		
		System.out.println("Before Increment Value of X is : "+x); // 20
		
		int y = ++x;
		System.out.println("After Increment Value of X is  : "+x); // 21
		
		System.out.println("What is the Value of Y  : "+y); //  21
		
		

	}

}
