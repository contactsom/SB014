package com.simplilearn.increment;

public class IncrementOperatorExample7 {

	public static void main(String[] args) {
	
		char x = 'a';
		
		System.out.println("Before Decrement Value of X is : "+x); // a

		char y = ++x;
				
		System.out.println("After Decrement Value of X is  : "+x); // b
				
		System.out.println("What is the Value of Y  : "+y); // b
		
		

	}

}
