package com.simplilearn.increment;

public class IncrementOperatorExample8 {

	public static void main(String[] args) {
	
		double x = 20.5;
		
		System.out.println("Before Decrement Value of X is : "+x); // 

		double y = ++x;
				
		System.out.println("After Decrement Value of X is  : "+x); //
				
		System.out.println("What is the Value of Y  : "+y); // 
		
		

	}

}
