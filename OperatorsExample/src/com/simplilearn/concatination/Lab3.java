package com.simplilearn.concatination;

public class Lab3 {

	public static void main(String[] args) {
		
		// Concatenation Operator 
		char bloodGroup1='A';
		char bloodGroup2='B';
		System.out.println(bloodGroup1+bloodGroup2);//65+66=131
		System.out.println(bloodGroup1+""+bloodGroup2);//AB
		
		// String is a combination of Character and space/blank space 
		
		String a = "";// blank space 
		String b = "";// blank space 
		System.out.println(a+b);
		
		
		
	}
}
