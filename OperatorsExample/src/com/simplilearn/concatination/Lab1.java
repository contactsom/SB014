package com.simplilearn.concatination;

public class Lab1 {

	public static void main(String[] args) {
		
		// Concatenation Operator only work for String
		
		String firstDigit= "1";
		String sendDigit= "2";
		System.out.println(firstDigit);
		System.out.println(sendDigit);
		System.out.println(firstDigit+sendDigit);// 12 
		
		int num1= 1;
		int num2= 2;
		System.out.println(num1+num2);//3
		
		
	}
}
